from jm_functions import (read_num_jobs, process_jobs)


###################################################################
################################# PATHS ###########################
###################################################################

from params import JOB_LIMIT, USER_NAME

###################################################################
############################# PARAMS ##############################
###################################################################


###################################################################
########################### FUNCTIONS #######
#######################
###################################################################

def main():
    current_jobs = read_num_jobs(USER_NAME)
    jobs_to_run = JOB_LIMIT - current_jobs
    if jobs_to_run == 0:
        print('No job slots available now.')
    else:
        process_jobs(jobs_to_run)

if __name__ == '__main__':
    main()